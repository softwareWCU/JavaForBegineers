package Lesson_02_Data_Types_and_Variables_in_Java_Program_Exercise;

public class PersonalDetails {
    public static void main(String[] args) {
        double height = 5.6; // in feet
        double weight = 60.5; // in kg
        char favoriteLetter = 'A';
        int decimal_valueof=(int)favoriteLetter;
        System.out.println("Height: " + height + " feet, Weight: " + weight + " kg, Favorite Letter: " + favoriteLetter + ", Decimal value of favorite letter is: "+decimal_valueof);
}
}